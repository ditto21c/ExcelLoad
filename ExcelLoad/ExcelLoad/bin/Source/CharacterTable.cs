using System;
using System.IO;
using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CPlayerInfo
{
	public int ID;
	public string Prefab;
	public ArrayList Model = new ArrayList();
	public int Name;

	public void Load(BinaryReader BinaryReader)
	{
		ID = BinaryReader.ReadInt32();
		Prefab = BinaryReader.ReadString();
		for(int i=0; i<2; ++i)
		{
			Model.Add(BinaryReader.ReadInt32());
		}
		Name = BinaryReader.ReadInt32();
	}
};

public class CEnemyInfo
{
	public int ID;
	public string Prefab;
	public ArrayList Model = new ArrayList();

	public void Load(BinaryReader BinaryReader)
	{
		ID = BinaryReader.ReadInt32();
		Prefab = BinaryReader.ReadString();
		for(int i=0; i<2; ++i)
		{
			Model.Add(BinaryReader.ReadInt32());
		}
	}
};

public class CPlayerInfoSheet
{
	public CPlayerInfo[] vec = new CPlayerInfo[18];
	public Dictionary<int, CPlayerInfo> map = new Dictionary<int, CPlayerInfo>();
};
public class CEnemyInfoSheet
{
	public CEnemyInfo[] vec = new CEnemyInfo[5];
	public Dictionary<int, CEnemyInfo> map = new Dictionary<int, CEnemyInfo>();
};
public class CCharacterTable
{
	public CPlayerInfoSheet PlayerInfoSheet = new CPlayerInfoSheet();
	public CEnemyInfoSheet EnemyInfoSheet = new CEnemyInfoSheet();

	public void Load()
	{
		TextAsset Asset = Resources.Load<TextAsset>("TextAssets/Tables/CharacterTable");
		Stream stream = new MemoryStream(Asset.bytes);
		BinaryReader BinaryReader = new BinaryReader(stream);
		for(int i=0; i<18; ++i)
		{
			PlayerInfoSheet.vec[i] = new CPlayerInfo();
			PlayerInfoSheet.vec[i].Load(BinaryReader);
			int key =PlayerInfoSheet.vec[i].ID;
			PlayerInfoSheet.map.Add(key, PlayerInfoSheet.vec[i]); 
		}
		for(int i=0; i<5; ++i)
		{
			EnemyInfoSheet.vec[i] = new CEnemyInfo();
			EnemyInfoSheet.vec[i].Load(BinaryReader);
			int key =EnemyInfoSheet.vec[i].ID;
			EnemyInfoSheet.map.Add(key, EnemyInfoSheet.vec[i]); 
		}
	}
};


































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































